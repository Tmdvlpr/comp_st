// Порядок предпочтения типа аномалии при выборе кейса (как в макете caseKey).
const KIND_PRIORITY: AnomalyKind[] = ['ml', 'drift', 'seasonal', 'roc']

// Сопоставление id датчика → группа кейса. Проверяется по подстроке в порядке списка.
const GROUP_MATCHERS: [string, string][] = [
  ['vibro', 'vibro'],
  ['axial_shift', 'axial_shift'],
  ['oil_temp', 'oil_temp'],
  ['gas_temp_out', 'gas_temp_out'],
  ['gas_temp', 'gas_temp_out'],
  ['rpm', 'rpm'],
  ['temp', 'temp'],
  ['pressure', 'pressure'],
]

function groupOf(sensorId: string): string {
  for (const [needle, group] of GROUP_MATCHERS) {
    if (sensorId.includes(needle)) return group
  }
  return sensorId
}

/**
 * Выбор карточки кейса по датчику и его активным типам аномалий (логика как в макете caseKey).
 * Группа берётся из id датчика, тип — первый активный kind по приоритету (ml → drift → seasonal → roc).
 * Если точного кейса 'группа|тип' нет — возвращается CASES.default.
 */
export function caseKey(sensorId: string, kinds: AnomalyKind[]): CaseInfo {
  const group = groupOf(sensorId)
  const kind = KIND_PRIORITY.find((k) => kinds.includes(k)) ?? kinds[0] ?? 'ml'
  return CASES[`${group}|${kind}`] ?? CASES.default
}
